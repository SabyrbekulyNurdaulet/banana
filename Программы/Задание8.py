# Даны два целых числа A и B (A < B). Вывести все целые числа, расположенные между данными числами (включая сами эти числа), в порядке их возрастания, а также количество N этих чисел. 


def numbers_between(a, b):
    # Проверка условия A < B
    if a >= b:
        raise ValueError("Число A должно быть меньше B.")
    
    # Создаем список для хранения чисел
    numbers = []
    
    # Добавляем числа от A до B в список
    for num in range(a, b + 1):
        numbers.append(num)
    
    # Выводим числа в порядке возрастания
    print("Числа между A и B включительно:")
    print(numbers)
    
    # Выводим количество чисел
    print(f"Количество чисел N: {len(numbers)}")

# Ввод чисел A и B с клавиатуры
try:
    A = int(input("Введите число A (A < B): "))
    B = int(input("Введите число B (A < B): "))
    numbers_between(A, B)
except ValueError:
    print("Ошибка: введите целые числа.")
except Exception as e:
    print(f"Произошла ошибка: {e}")
