# Создайте массив, содержащий 10 первых нечетных чисел. Выведете элементы массива на консоль в одну строку, разделяя запятой.


# Создаем пустой массив для хранения нечетных чисел
odd_numbers = []

# Начальное нечетное число
current_odd = 1

# Цикл для добавления первых 10 нечетных чисел
while len(odd_numbers) < 10:
    odd_numbers.append(current_odd)
    current_odd += 2  # Переходим к следующему нечетному числу

# Вывод элементов массива в одну строку, разделяя запятой
print(", ".join(map(str, odd_numbers)))
