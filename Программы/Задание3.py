# Ввести с клавиатуры два числа. Определить, что больше, сумма квадратов или квадрат суммы этих чисел. Ответ вывести в виде сообщения


# Ввод двух чисел с клавиатуры
a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))

# Вычисление суммы квадратов
sum_of_squares = a**2 + b**2

# Вычисление квадрата суммы
square_of_sum = (a + b)**2

# Сравнение и вывод результата
if sum_of_squares > square_of_sum:
    print("Сумма квадратов больше квадрата суммы.")
elif sum_of_squares < square_of_sum:
    print("Квадрат суммы больше суммы квадратов.")
else:
    print("Сумма квадратов равна квадрату суммы.")
